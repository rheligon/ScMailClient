    private void mostrarPlantilla() {
        Configuracion conf = ManejadorBD.getConfiguracion1();
        
        AsuntoInput.setText(conf.getPlantillaInputAsunto());
        EncabezadoInput.setText(conf.getPlantillaInputEncabezado());
        FirmaInput.setText(conf.getPlantillaInputFirma());
        AsuntoOutput.setText(conf.getPlantillaOutputAsunto());
        EncabezadoOutput.setText(conf.getPlantillaOutputEncabezado());
        FirmaOutput.setText(conf.getPlantillaOutputFirma());
    }
    
