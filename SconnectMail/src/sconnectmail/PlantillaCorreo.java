private void AceptarPlantillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarPlantillaActionPerformed
        Configuracion conf = new Configuracion();
        
        conf.setPlantillaInputAsunto(AsuntoInput.getText());
        conf.setPlantillaInputEncabezado(EncabezadoInput.getText());
        conf.setPlantillaInputFirma(FirmaInput.getText());
        conf.setPlantillaOutputAsunto(AsuntoOutput.getText());
        conf.setPlantillaOutputEncabezado(EncabezadoOutput.getText());
        conf.setPlantillaOutputFirma(FirmaOutput.getText());
        
        if(ManejadorBD.setPlantilla(conf)){OptionPane.showMessageDialog(null,"Se ha actualizado la plantilla","Información",JOptionPane.INFORMATION_MESSAGE); setVisible(false);}       
}//GEN-LAST:event_AceptarPlantillaActionPerformed

