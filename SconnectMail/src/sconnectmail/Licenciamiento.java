    private void CargarLicenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarLicenciaActionPerformed
              
        String linea;
        String firma = "";
        String salt = "";
        String fechaExpiracion = "";
        String bic = "";
        String stringTotal = "";
        StringTokenizer tok;
             
        try {
            File file = new File(ArchivoLicencia.getText());
            FileReader reader = new FileReader(file);
            BufferedReader in = new BufferedReader(reader);

            while((linea = in.readLine()) != null){
                if(linea.contains("BIC")){
                    tok = new StringTokenizer(linea,">");
                    tok.nextToken();
                    tok = new StringTokenizer(tok.nextToken(),"<");
                    bic = tok.nextToken();
                    stringTotal = stringTotal + bic;
                }       
                else if(linea.contains("FechaExpiracion")){
                    tok = new StringTokenizer(linea,">");
                    tok.nextToken();
                    tok = new StringTokenizer(tok.nextToken(),"<");
                    fechaExpiracion = tok.nextToken();
                    stringTotal = stringTotal + fechaExpiracion;
                }
                else if(linea.contains("*")){
                    salt = linea.substring(1,linea.length());             
                }
                else if(linea.contains("$")){
                    firma = linea.substring(1,linea.length());
                }
            }
            
            Licencia licencia = new Licencia();
              
            licencia.setBic(bic);     
            licencia.setFechaVencimiento(formatoFecha(fechaExpiracion));
            licencia.setSalt(salt);
            licencia.setHash(firma);
            
            ManejadorBD.setLicencia(licencia);                     
            OptionPane.showMessageDialog(null,"La licencia fue cargada con éxito","Aviso",JOptionPane.INFORMATION_MESSAGE);
            setVisible(false); 
                        
        } catch (IOException e) {
            
        }
    }//GEN-LAST:event_CargarLicenciaActionPerformed

