    public static Date formatoFecha(String info){

            Date fecha = new Date();
            
            fecha.setDate(Integer.parseInt(info.substring(0,2)));
            fecha.setMonth(Integer.parseInt(info.substring(3,5))-1);            
            fecha.setYear(Integer.parseInt(info.substring(6,10))-1900);

            return fecha;

     }
    
