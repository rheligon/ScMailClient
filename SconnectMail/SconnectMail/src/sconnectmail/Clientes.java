private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        if(paramVacios()){OptionPane.showMessageDialog(null,"Debe completar todos los campos","Error",JOptionPane.ERROR_MESSAGE); }
        else{
        int opcion = OptionPane.showConfirmDialog(null,"Â¿EstÃ¡ seguro que desea modificar el registro?","Advertencia",JOptionPane.YES_NO_OPTION);  //0 Aceptar

        if (opcion == 0){       
       
            Vector datos = new Vector();
            
            datos.add(Cliente.getText());
            datos.add(Correo.getText());
            
            if(agregar){
                datos.add("");
            }
            else{
                datos.add(ClientesTable.getValueAt(ClientesTable.getSelectedRow(), 0).toString());
            }           
           
            ManejadorBD.setCliente(datos);
            ManejadorBD.updateClientes(ClientesTable);
            agregar = false;
        }
        }
}//GEN-LAST:event_AceptarActionPerformed

